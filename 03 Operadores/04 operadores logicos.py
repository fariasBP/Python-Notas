# OPERADORES LOGICOS
#     Se utiliza un operador lógico para tomar una decisión basada en múltiples
#     condiciones. El operador lógico devuelve un valor booleano (true o false).
#     OPERADORES:
#         - and ---> And
#         - or ---> Or
#         - not ---> Not
#     EJEMPLO:
#         and	Devuelve True si ambos operandos son True	(12 > 3 and 12 < 3) devuelve False
#         or	Devuelve True si alguno de los operandos es True	(12 > 3 or 12 < 3) devuelve True
#         not	Devuelve True si el operando es False	(not 12 > 3) devuelve False
        
print(12 > 3 and 12 < 3)
print(12 > 3 or 12 < 3)
print(not 12 > 3)