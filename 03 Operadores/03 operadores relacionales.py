# OPERADORES RELACIONALES
#     Un operador relacional se emplea para comparar y establecer la relación
#     entre ellos. Devuelve un valor booleano (true o false) basado en la
#     condición.
#     OPERADORES:
#         - == ---> Igual
#         - != ---> Diferente
#         - > ---> Mayor que
#         - < ---> Menor que
#         - >= ---> Mayor o igual que
#         - <= ---> Menor o igual que
#     EJEMPLO:
#         >	Devuelve True si el valor de la izquierda es mayor que el valor de la derecha	12 > 3 devuelve True
#         <	Devuelve True si el valor de la derecha es mayor que el valor de la izquierda	12 < 3 devuelve False
#         ==	Devuelve True si ambos operandos son iguales	12 == 3 devuelve False
#         >=	Devuelve True si el valor de la izquierda es mayor o igual que el valor de la derecha	12 >= 3 devuelve True
#         <=	Devuelve True si el valor de la derecha es mayor o igual que el valor de la izquierda	12 <= 3 devuelve False
#         !=	Devuelve True si ambos operandos no son iguales	12 != 3 devuelve True
        
print(12 > 3)
print(12 < 3)
print(12 == 3)
print(12 >= 3)
print(12 <= 3)
print(12 != 3)