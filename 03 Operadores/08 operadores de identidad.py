# OPERADORES DE IDENTIDAD
#     Un operador de identidad se utiliza para verificar si dos objetos son el mismo
#     objeto, es decir se emplea para comprobar si dos variables emplean la misma ubicación
#     en memoria. Devuelve un valor booleano (true o false) basado en la condición.
#     OPERADORES:
#         - is ---> Identidad
#         - is not ---> No identidad
#     EJEMPLO:
#         is	Devuelve True si ambos operandos son el mismo objeto	3 is 3 devuelve True
#         is not	Devuelve True si ambos operandos no son el mismo objeto	3 is not 3 devuelve False
        
print(3 is 3)
print(3 is not 3)