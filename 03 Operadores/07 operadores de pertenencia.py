# OPERADORES DE PERTENENCIA
#     Un operador de pertenencia se utiliza para verificar si un elemento pertenece
#     a una secuencia (list, tuple, string). Devuelve un valor booleano (true o false)
#     basado en la condiciÃ³n.
#     OPERADORES:
#         - in ---> Pertenece
#         - not in ---> No pertenece
#     EJEMPLO:
#         in	Devuelve True si el operando pertenece a la secuencia	3 in [1, 2, 3] devuelve True
#         not in	Devuelve True si el operando no pertenece a la secuencia	3 not in [1, 2, 3] devuelve False

print(3 in [1, 2, 3])
print(3 not in [1, 2, 3])