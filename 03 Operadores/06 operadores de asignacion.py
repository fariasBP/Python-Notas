# OPERADORES DE ASIGNACION
#     Se utiliza un operador de asignación para asignar valores a una variable. Esto
#     generalmente se combina con otros operadores (como aritmética, bit a bit) donde
#     la operación se realiza en los operandos y el resultado se asigna al operando
#     izquierdo.
    
#     OPERADORES:
#         - = ---> Asignacion 
# 		  - += ---> Asignacion mas o cacantenacion
# 		  - -= ---> Asignacion menos
# 		  - *= ---> Asignacion por
# 		  - /= ---> Asignacion dividido
# 		  - %= ---> Asignacion de resto
#         - **= ---> Asignacion de potencia
#         - //= ---> Asignacion de cociente
#         - &= ---> Asignacion de and
#         - |= ---> Asignacion de or
#         - ^= ---> Asignacion de xor
#         - >>= ---> Asignacion de desplazamiento a la derecha
#         - <<= ---> Asignacion de desplazamiento a la izquierda
    
#     EJEMPLO TEXTUAL:
#         =	a = 5. El valor 5 es asignado a la variable a
#         +=	a += 5 es equivalente a a = a + 5
#         -=	a -= 5 es equivalente a a = a - 5
#         *=	a *= 3 es equivalente a a = a * 3
#         /=	a /= 3 es equivalente a a = a / 3
#         %=	a %= 3 es equivalente a a = a % 3
#         **=	a **= 3 es equivalente a a = a ** 3
#         //=	a //= 3 es equivalente a a = a // 3
#         &=	a &= 3 es equivalente a a = a & 3
#         |=	a |= 3 es equivalente a a = a | 3
#         ^=	a ^= 3 es equivalente a a = a ^ 3
#         >>=	a >>= 3 es equivalente a a = a >> 3
#         <<=	a <<= 3 es equivalente a a = a << 3

a = 18

a += 5
print(a)

a -= 5
print(a)

a *= 3
print(a)

a /= 3
print(a)

a %= 3
print(a)

a **= 3
print(a)

a //= 3
print(a)

a = 10
a &= 3
print(a)

a |= 3
print(a)

a ^= 3
print(a)

a >>= 3
print(a)

a <<= 3
print(a)

