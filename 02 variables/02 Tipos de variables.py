# TIPOS DE DATOS
#     NUMEROS: Los tipos numéricos se utilizan para representar números en Python.
#         Entero (int): Los enteros son números enteros sin ningún punto decimal. Ejemplo: 5, 10, -3.
#         Flotante (float): Los flotantes son números con punto decimal. Ejemplo: 3.14, -2.5.
#         Complejo (complex): Los números complejos son números con una parte real y otra imaginaria.
#     CADENAS (str): Los tipos de cadenas se utilizan para representar texto en Python.
#     BOOLEANOS (bool): Los tipos booleanos se utilizan para representar verdadero o falso en Python.
#     LISTAS (list): Los tipos de listas se utilizan para representar colecciones de elementos en Python.
#     TUPLAS (tuple): Los tipos de tuplas se utilizan para representar colecciones de elementos fijos (INMUTABLES) en Python.
#     CONJUNTOS (set): Los tipos de conjuntos se utilizan para representar colecciones de elementos sin duplicados en Python.
#     DICCIONARIOS (dict): Los tipos de diccionarios se utilizan para representar colecciones de elementos clave-valor en Python.    

# STR - cadena de texto
cadena = "text"
cadena2 = 'otro texto'

# NUMEROS
# int - numeros enteros
entero = 1
# float - numeros flotantes
flotante = 1.2
# complex - numeros complejos
complejo = 1+2j

# BOOL - booleanos (True:verdader y False:falso)
booleano = True
booleano2 = False

# LIST - listas []
lista = [1,2,3,4]
lista2 = ["uno", 2, 'tres', 4]

# TUPLE - tuplas ()
tupla = (1,2,3,4)
tupla2 = ("uno", 2, 'tres', 4)

# DICT - diccionarios {}
diccionario = {"clave": "valor", "clave2": 2, "clave3": "tres"}
diccionario2 = {1: "uno", 2: 2, 3: "tres"}

print(type(cadena))
print(cadena)
print(type(cadena2))
print(cadena2)
print(type(entero))
print(entero)
print(type(flotante))
print(flotante)
print(type(complejo))
print(complejo)
print(type(booleano))
print(booleano)
print(type(lista))
print(lista)
print(type(lista2))
print(lista2)
print(type(tupla))
print(tupla)
print(type(tupla2))
print(tupla2)
print(type(diccionario))
print(diccionario)
print(type(diccionario2))
print(diccionario2)