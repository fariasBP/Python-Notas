# CONSTANTES
#     Python no tiene una sintaxis dedicada para la declaración de constantes. Por tanto,
#     tampoco tiene constantes en el sentido estricto de la palabra.
#     Así, para definir una constante en Python tienes que usar una variable y asumir
#     que nunca va a cambiar.

PI = 3.14592
SEGUNDOS_HORA = 3600
ANCHURA_VENTANA = 760

print(f'{PI=}')
print(f'{SEGUNDOS_HORA=}')
print(f'{ANCHURA_VENTANA=}')